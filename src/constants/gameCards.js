// Helper functions for card management
export const getRandomSlightCard = () => {
  const randomIndex = Math.floor(Math.random() * SLIGHT_CARDS.length);
  return SLIGHT_CARDS[randomIndex];
};

export const getRandomCurseCards = (count = 7) => {
  const shuffled = [...CURSE_CARDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getAllSlightCards = () => {
  return [...SLIGHT_CARDS];
};

export const getAllCurseCards = () => {
  return [...CURSE_CARDS];
};

export const getCardById = (id) => {
  return [...SLIGHT_CARDS, ...CURSE_CARDS].find(card => card.id === id);
};

export const getCardsByType = (type) => {
  if (type === 'slight') {
    return [...SLIGHT_CARDS];
  } else if (type === 'curse') {
    return [...CURSE_CARDS];
  }
  return [];
};

export const shuffleCards = (cards) => {
  return [...cards].sort(() => Math.random() - 0.5);
};

